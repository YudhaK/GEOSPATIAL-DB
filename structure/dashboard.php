<h5>Direktorat Jenderal Perkeretaapian dalam PM No. 189 Tahun 2015</h5> <br>
<b>Pasal 555</b> <br>
(1)  Direktorat  Jenderal  Perkeretaapian  berada  di  bawah dan bertanggung jawab kepada Menteri. <br>
(2)  Direktorat  Jenderal  Perkeretaapian  dipimpin  oleh  Direktur Jenderal. <br>
<b>Pasal 556</b> <br>
Direktorat  Jenderal  Perkeretaapian  mempunyai  tugas menyelenggarakan  perumusan  dan  pelaksanaan  kebijakan di bidang perkeretaapian. <br>
<br>
<b>Visi dan Misi</b> <br>

<b>1.  Misi</b> <br>
Mewujudkan eksistensi sebagai regulator dan penyelenggaraan perkeretaapian multioperator guna terselenggaranya pelayanan angkutan kereta api secara massal yang menjamin keselamatan, aman, nyaman, cepat dan lancar, tertib dan teratur, efisien, terpadu dengan moda transportasi lain, serta menunjang pemerataan, pertumbuhan, stabilitas, pendorong, dan penggerak pembangunan nasional.<br>
<b>2.  Visi</b> <br>
1.  Meningkatkan peran Pemerintah sebagai regulator penyelenggaraan perkeretaapian. <br>
2.  Mewujudkan penyelenggaraan perkeretaapian multioperator dengan peningkatan peran Pemerintah Daerah dan swasta. <br>
3.  Meningkatkan peran Kereta Api sebagai angkutan publik. <br> 
4.  Meningkatkan peran Kereta Api sebagai tulang punggung angkutan barang. <br> 
5.  Meningkatkan peran Kereta Api sebagai pelopor terciptanya angkutan terpadu. <br> <br>

<b>Tugas Pokok dan Fungsi</b> <br>

Merumuskan serta melaksanakan kebijakan dan standarisasi teknis di bidang perkeretaapian merupakan Tugas Pokok dari Direktorat Jenderal Perkeretaapian. Adapun fungsi-fungsinya sebagai berikut : <br>
1.  Penyiapan perumusan kebijakan Kementerian Perhubungan di bidang lalu lintas dan angkutan kereta api, teknik prasarana, keselamatan dan teknik sarana kereta api. <br>
2.  Pelaksanaan kebijakan di bidang lalu lintas dan angkutan kereta api, teknik prasarana, keselamatan dan teknik sarana kereta api. <br>
3.  Penyusunan standar, norma, pedoman, kriteria dan prosedur di bidang lalu lintas dan angkutan kereta api, teknik prasarana, keselamatan dan teknik sarana kereta api. <br>
4.  Pemberian bimbingan teknis dan evaluasi di bidang perkeretaapian. <br>
5.  Pelaksanaan administrasi Direktorat Jenderal Perkeretaapian. <br> <br>

